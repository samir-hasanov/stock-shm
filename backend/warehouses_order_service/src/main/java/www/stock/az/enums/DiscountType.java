package www.stock.az.enums;

/**
 * Discount type enumeration
 */
public enum DiscountType {
    /**
     * Percentage-based discount (e.g., 10%)
     */
    PERCENTAGE,

    /**
     * Fixed amount discount (e.g., 50 AZN)
     */
    FIXED_AMOUNT,

    /**
     * Buy X get Y free
     */
    BUY_X_GET_Y,

    /**
     * Free shipping
     */
    FREE_SHIPPING
}
